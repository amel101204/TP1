public class Main_2702507391_5a{
    public static void main(String[] args) {
        int a = 7;
        int b = 3;
        
        // Operator Relasional 
        boolean hasil1 = (a == b); // apakah a sama dengan b?
        boolean hasil2 = (a != b); // apakah a tidak sama dengan b? 
        boolean hasil3 = (a > b); // apakah a lebih besar dari b?
        boolean hasil4 = (a >= b); // apakah a lebih kecil dari b?
        boolean hasil5 = (a >= b); // apakah a lebih besar atau sama dengan b?
        boolean hasil6 = (a <= b); // apakah a lebih kecil atau sama dengan b?
        
        // Menampilkan hasil
        System.out.println("apakah a sama dengan b?" + hasil1);
        System.out.println("apakah a tidak sama dengan b?" + hasil2);
        System.out.println("apakah a lebih besar dari b?" + hasil3);
        System.out.println("apakah a lebih kecil dari b?" + hasil4);
        System.out.println("apakah a lebih besar atau sama dengan b?" + hasil5);
        System.out.println("apakah a lebih kecil atau sama dengan b?" + hasil6);
        
    }
}